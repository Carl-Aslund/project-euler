def largestProduct(n):
    """Finds the largest product of n consecutive digits in the above number."""
    digits = [int(x) for x in number]
    maxProduct = 0
    for i in range(len(digits)-n+1):
        product = 1
        for j in range(i, i+n):
            product *= digits[j]
        maxProduct = max(maxProduct, product)
    return maxProduct

